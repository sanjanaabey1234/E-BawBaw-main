// Database connection
$host = 'localhost';
$db = 'ebawbawdb'; // Replace with your database name
$user = 'root'; // Replace with your database username
$pass = ''; // Replace with your database password

try {
    $pdo = new PDO("mysql:host=$host;dbname=$db;charset=utf8", $user, $pass);
    $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
} catch (PDOException $e) {
    die("Could not connect to the database $db :" . $e->getMessage());
}

// Fetch data for dashboard
// New Animal Count Monthly
$animalStmt = $pdo->prepare("SELECT COUNT(*) FROM animals WHERE MONTH(created_at) = MONTH(CURRENT_DATE()) AND YEAR(created_at) = YEAR(CURRENT_DATE())");
$animalStmt->execute();
$newAnimalCount = $animalStmt->fetchColumn();

// Donation Count Monthly
$donationStmt = $pdo->prepare("SELECT COUNT(*) FROM donors WHERE MONTH(reg_date) = MONTH(CURRENT_DATE()) AND YEAR(reg_date) = YEAR(CURRENT_DATE())");
$donationStmt->execute();
$donationCount = $donationStmt->fetchColumn();

// User Registrations Monthly
$userStmt = $pdo->prepare("SELECT COUNT(*) FROM users WHERE MONTH(date_created) = MONTH(CURRENT_DATE()) AND YEAR(date_created) = YEAR(CURRENT_DATE())");
$userStmt->execute();
